# Data
nama_list = ['Ayu', 'Budi', 'Citra', 'Ayu']
nama_tuple = ('Dina', 'Eko', 'Dina', 'Fajar')

# Konversi
print("List:", nama_list)
print("List menjadi Set:", set(nama_list))
print("Set menjadi List:", list(set(nama_list)))

print("Tuple:", nama_tuple)
print("Tuple menjadi Set:", set(nama_tuple))
print("Set menjadi Tuple:", tuple(set(nama_tuple)))
